    """
    This function generates a random name by combining a left and right part with an optional separator.

    Args:
    - sep (str, optional): The separator to use between the left and right parts. Defaults to '-'.

    Returns:
    - str: A random name generated by combining a randomly chosen left part and right part separated by the separator.
    """
    return f"{random.choice(left)}{sep}{random.choice(right)}"


